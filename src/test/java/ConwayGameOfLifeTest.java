import org.junit.jupiter.api.Test;

import static org.junit.jupiter.api.Assertions.*;

class ConwayGameOfLifeTest {

    String[][] myGrid = {{"+","+","#","+","+","+","+","+","+","+"},
                         {"+","+","#","#","#","+","+","+","+","+"},
                         {"+","+","+","+","+","+","+","+","+","+"},
                         {"+","+","+","+","+","+","+","+","+","+"},
                         {"+","+","+","+","+","+","+","+","+","+"},
                         {"+","+","+","+","+","+","+","+","+","+"},
                         {"+","+","+","+","+","+","+","+","+","+"},
                         {"+","+","+","+","+","+","+","+","+","+"},
                         {"+","+","+","+","+","+","+","+","+","+"},
                         {"+","+","+","+","+","+","+","+","+","+"},};

    String[][] expectedGen = {{"+","+","#","+","+","+","+","+","+","+"},
                              {"+","+","#","#","+","+","+","+","+","+"},
                              {"+","+","+","#","+","+","+","+","+","+"},
                              {"+","+","+","+","+","+","+","+","+","+"},
                              {"+","+","+","+","+","+","+","+","+","+"},
                              {"+","+","+","+","+","+","+","+","+","+"},
                              {"+","+","+","+","+","+","+","+","+","+"},
                              {"+","+","+","+","+","+","+","+","+","+"},
                              {"+","+","+","+","+","+","+","+","+","+"},
                              {"+","+","+","+","+","+","+","+","+","+"},};

    @Test
    void nextGen() {
        ConwayGameOfLife life = new ConwayGameOfLife();
        boolean results = true;
        String[][] testCase = life.nextGen(myGrid);
        for (int i = 0; i < 10; i++) {
            for (int j = 0; j < 10; j++) {
                if (!expectedGen[i][j].equals(testCase[i][j])) {
                    results = false;
                }
            }
        }
        assertTrue(results);
    }
}